		public static void GenerateMeshData(this QVoxel voxelData,bool gup=true)
		{
			if (voxelData.MeshData.Changing||!voxelData.MeshData.Dirty) return;
			voxelData.MeshData.Clear();
			voxelData.MeshData.Changing = true;
			if (gup)
			{
				GenerateGPU(voxelData);
			}
			else
			{
				GenerateCPU(voxelData);
			}
			voxelData.MeshData.Changing = false;
			voxelData.MeshData.FreshMesh();
		}
		
		#region GUP
		public static ComputeShader Shader;
		static void GenerateGPU(QVoxel voxelData)
		{
			if (Shader == null)
			{
				Shader = Resources.Load<ComputeShader>(nameof(QMarchingCubes) + nameof(Shader));
			}
			var size = Mathf.Max(voxelData.Size.x, voxelData.Size.y, voxelData.Size.z)+2 ;
			Vector4[] Voxels = new Vector4[size* size* size];
			var index = 0;
			for (int x = voxelData.Min.x - 1; x < voxelData.Min.x-1 +size; x++)
			{
				for (int y = voxelData.Min.y - 1; y < voxelData.Min.y -1+ size; y++)
				{
					for (int z = voxelData.Min.z - 1; z <  voxelData.Min.z-1 + size; z++)
					{
						Voxels[index++] = new Vector4(x, y, z, voxelData[x, y, z]);
					}
				}
			}
			using (var VoxelsBuffer = new ComputeBuffer(Voxels.Length, sizeof(float) * 4))
			{
				using (var TrianglesBuffer = new ComputeBuffer(Voxels.Length * 5, sizeof(float) *( 3*3+1) , ComputeBufferType.Append))
				{
					TrianglesBuffer.SetCounterValue(0);
					int kernelHandle = Shader.FindKernel("CSMain");
					Shader.SetFloat(nameof(voxelData.Surface), voxelData.Surface);
					Shader.SetInt("Size", size);

					VoxelsBuffer.SetData(Voxels);
					Shader.SetBuffer(kernelHandle, nameof(VoxelsBuffer), VoxelsBuffer);
					Shader.SetBuffer(kernelHandle, nameof(TrianglesBuffer), TrianglesBuffer);
					Shader.Dispatch(kernelHandle, Mathf.CeilToInt(size/ 8f), Mathf.CeilToInt(size / 8f), Mathf.CeilToInt(size / 8f));
					using (var CountBuffer = new ComputeBuffer(1, sizeof(int), ComputeBufferType.Raw))
					{
						ComputeBuffer.CopyCount(TrianglesBuffer, CountBuffer, 0);
						int[] countArray = { 0 };
						CountBuffer.GetData(countArray);
						TriangleData[] triangles = new TriangleData[countArray[0]];
						TrianglesBuffer.GetData(triangles);
						foreach (var t in triangles)
						{
							voxelData.MeshData.AddTriangle(t.a, t.b, t.c, voxelData.GetColor(t.value));
						}
					}
				}
			}

		}
		struct TriangleData
		{
			public Vector3 a;
			public Vector3 b;
			public Vector3 c;
			public float value;
		};
		#endregion

		#region CPU
		static void GenerateCPU(QVoxel voxelData)
		{
			for (int x = voxelData.Min.x - 1; x <= voxelData.Max.x; x++)
			{
				for (int y = voxelData.Min.y - 1; y <= voxelData.Max.y; y++)
				{
					for (int z = voxelData.Min.z - 1; z <= voxelData.Max.z; z++)
					{
						var pos = new Vector3Int(x, y, z);
						GenerateCube(voxelData, pos);
					}
				}
			}
		}
		static void GenerateCube(QVoxel voxelData, Vector3Int pos)
		{
			int cubeIndex = 0;
			var cube = new Vector4[8];
			Color32 color = Color.clear;
			for (var i = 0; i < 8; i++)
			{
				var tPos = pos + VertexOffset[i];
				cube[i] = new Vector4(tPos.x, tPos.y, tPos.z, voxelData[tPos]);
				if (cube[i].w <= voxelData.Surface) { cubeIndex |= 1 << i; }
				else if (color.a <= 0)
				{
					color = voxelData.GetColor(cube[i].w);
				} 
			}
			for (int i = 0; TriangleTable[cubeIndex, i] != -1; i += 3)
			{
				for (int pointIndex = 0; pointIndex < 3; pointIndex++)
				{
					var point = GetPoint(
						cube[PointA[TriangleTable[cubeIndex, i + pointIndex]]],
						cube[PointB[TriangleTable[cubeIndex, i + pointIndex]]],
						voxelData.Surface);
					voxelData.MeshData.AddPoint(point, color);
				}
			}
		}
		static Vector3 GetPoint(Vector4 a, Vector4 b, float surface)
		{
			float t = (surface - a.w) / (b.w - a.w);
			return a + t * (b - a);
		}

